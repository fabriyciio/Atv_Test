public class Calculadora{
   



    public int Soma( int number1, int number2 ){
        return number1 + number2;
    }

    public int Subtração( int number1, int number2 ){
        return number1 - number2;
    }

    public int Divesao( int number1, int number2 ){
        return number1 / number2 ;
    }

    public int Mutiplicação( int number1, int number2 ){
        return number1 * number2 ;
    }

    
}